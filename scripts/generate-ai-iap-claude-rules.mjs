#!/usr/bin/env node
/**
 * Generates `.claude/rules/` from `plugin/lib/rules/` like `/ai-iap:setup` Step 7b + 7d.
 *
 *   node scripts/generate-ai-iap-claude-rules.mjs
 *
 * Edit SELECTION for language/documentation toggles. Processes/frameworks are not generated here;
 * add selection logic if this repo starts using them.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..");
const PLUGIN_LIB = path.join(REPO_ROOT, "plugin", "lib");
const CONFIG_PATH = path.join(PLUGIN_LIB, "config.json");
const RULES_LIB = path.join(PLUGIN_LIB, "rules");
const OUT_ROOT = path.join(REPO_ROOT, ".claude", "rules");

/** Mirrors `.ai-iap-state.json` selections for this repository. */
const SELECTION = {
  selectedLanguages: ["general", "json", "yaml", "bash", "powershell", "javascript"],
  /** Keys of `languages.general.documentation` in config (e.g. code, project, api) */
  selectedDocumentation: ["code", "project"],
  enableCommitStandards: true,
};

const BANNER =
  "<!-- Generated by AI Instructions and Prompts Setup (ai-iap plugin) -->\n\n";

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, "utf8"));
}

function globPatternsToPaths(globsStr) {
  return globsStr.split(",").map((s) => s.trim()).map((p) => {
    if (p.includes("/") || p.includes("\\")) return p.startsWith("**/") ? p : `**/${p}`;
    return `**/${p}`;
  });
}

function writeManagedMd(outPath, body, { yamlPaths } = {}) {
  const front = yamlPaths?.length
    ? `---\naiIapManaged: true\npaths:\n${yamlPaths.map((p) => `  - ${JSON.stringify(p)}`).join("\n")}\n---\n\n`
    : `---\naiIapManaged: true\n---\n\n`;
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, front + BANNER + body, "utf8");
}

function readRule(relativeFromRulesLib) {
  const fp = path.join(RULES_LIB, relativeFromRulesLib);
  if (!fs.existsSync(fp)) {
    throw new Error(`Missing rule file: ${path.relative(REPO_ROOT, fp)}`);
  }
  return fs.readFileSync(fp, "utf8");
}

function main() {
  const config = readJson(CONFIG_PATH);
  const { selectedLanguages, selectedDocumentation, enableCommitStandards } = SELECTION;

  let count = 0;

  for (const lang of selectedLanguages) {
    const L = config.languages[lang];
    if (!L) throw new Error(`Unknown language in selection: ${lang}`);

    const always = L.alwaysApply === true;
    const yamlPaths = always ? undefined : globPatternsToPaths(L.globs);

    for (const stem of L.files) {
      if (stem === "commit-standards" && !enableCommitStandards) continue;

      const content = readRule(path.join(lang, `${stem}.md`));
      const out = path.join(OUT_ROOT, "core", lang, `${stem}.md`);
      writeManagedMd(out, content, { yamlPaths });
      count++;
    }
  }

  const docBundle = config.languages.general.documentation;
  for (const docKey of selectedDocumentation) {
    const entry = docBundle[docKey];
    if (!entry) {
      throw new Error(`Unknown documentation key: ${docKey} (valid: ${Object.keys(docBundle).join(", ")})`);
    }
    const relPath = path.join("general", `${entry.file}.md`);
    const content = readRule(relPath);
    const base = path.basename(entry.file);
    const out = path.join(OUT_ROOT, "core", "documentation", `${base}.md`);
    writeManagedMd(out, content, {});
    count++;
  }

  console.log(`Wrote ${count} rule files under ${path.relative(REPO_ROOT, OUT_ROOT)}/`);
}

main();
